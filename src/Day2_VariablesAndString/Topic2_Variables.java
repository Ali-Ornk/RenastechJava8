package Day2_VariablesAndString;

public class Topic2_Variables {
    public static void main(String[] args) {
        /*
        2023
        123450000
        3.85
        1
        0
        0.000012345

        double = a number with decimal point


         */
        // Type VariableName = Value;
        double Year = 2023;

        System.out.println(Year);

        Year = 2024;

        double Salary = 123450000;

        double Degree = 3.85;

        double YesOrNo = 1;

        // Variable Type
        // Type VariableName = Value;
        byte ByteValue = 100;
        // byte values will be between -128 to 127 (Numeric)
        // byte AnotherByteValue = 1000; <- this is not correct

        short ShortValue = 2024;
        //short values will be between -32768 to 32767 (Numeric)
        short AnotherShortValue = -32000;

        int IntValue = 5;  //<- a numeric variable use int
        //int values will be between -2billion to 2 billion (Numeric)

        int MyAge = 34;
        // Type VariableName = Value;
        int Value1 = 25;

        //int value 2= 33;  <- you can not use space when you define variable name

        int Date_of_Birth = 2020;

        double Pi = 3.14654932;
        // double can hold 16 digits after decimal point

        char CharValue = 'Y';  // T = 84
        System.out.println(CharValue);

        // these all are primitive data type

    }
}
